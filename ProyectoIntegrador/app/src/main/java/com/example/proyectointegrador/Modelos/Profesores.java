package com.example.proyectointegrador.Modelos;

public class Profesores {
    public String idProfesor;
    public String NombreP;
    public String ApellidoPP;
    public String ApellidoMP;
    public int NumeroTelP;


    public Profesores() {
    }

    public Profesores(String idProfesor, String NombreP, String ApellidoPP, String ApellidoMP, int NumeroTelP) {
        idProfesor = idProfesor;
        NombreP = NombreP;
        ApellidoPP = ApellidoPP;
        ApellidoMP = ApellidoMP;
        NumeroTelP = NumeroTelP;
    }
}
