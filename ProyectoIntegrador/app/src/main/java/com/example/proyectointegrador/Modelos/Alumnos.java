package com.example.proyectointegrador.Modelos;

public class Alumnos {
    public String idAlumno;
    public String Nombre;
    public String ApellidoP;
    public String ApellidoM;
    public int NumeroTel;


    public Alumnos() {
    }

    public Alumnos(String idAlumno, String Nombre, String ApellidoP, String ApellidoM, int numeroTel) {
        idAlumno = idAlumno;
        Nombre = Nombre;
        ApellidoP = ApellidoP;
        ApellidoM = ApellidoM;
        NumeroTel = NumeroTel;
    }
}
